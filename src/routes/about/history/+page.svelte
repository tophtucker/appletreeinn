		<p>
			In late 1958, Rappaport sold the property to the Avaloch Realty trust, with trustee L. George
			Reder. On April 8, 1959, Avaloch Realty Trust sold it to Frederick M. Myers, Jr.
		</p>

		<p>
			For the 1959 season only it was operated by Don Soviero (I’m not sure if there was another
			sale or if Fred Myers & Don Soviero were partners?) who owned Bousquet Mountain and later went
			on to run the famous Music Inn, host of a litany of world-class jazz, folk, blues, and rock
			performers. On September 30, 1959, Soviero sold Avaloch to Michael Bakwin, trustee of
			Berkshire Avaloch Realty Trust in a swap for a property he owned on Rte 183 in Stockbridge
			that I don’t really understand yet but I think has something to do with the Music Inn.
		</p>

		<p>
			In 1963, Bakwin added the Round Room, originally as a porch I think, and in 1966 built the
			21-room structure that is now called the Annex (alternately the Lodge and the Motel). At some
			point he also built a small ski lift, the top of which is still there in the woods behind the
			hotel! He also continued to run the Five Reasons with success and played host to summer
			residencies with jazz greats such as Randy Weston.
		</p>

		<figure>
			<img src="../history/image2.png" />
			<figcaption>
				Berkshire Eagle clip from the opening of the Round Room (then the Gazebo), 1963
			</figcaption>
		</figure>

		<p>
			In February 1970, Bakwin sold the property to Max Wasserman (Wasserman Development Corp.) who
			demolished the East House (and part of the East House carriage house) that March and tried but
			failed to turn the property into a resort (or condos?). On July 10, 1974, Wasserman sold it to
			Richard E. Ross.
		</p>

		<p>
			Ross sold it to Alice Brock, made famous by Arlo Guthrie’s folk anthem Alice’s Restaurant. She
			ran the property under the name “Alice’s at Avaloch” and it was the third and final Alice’s
			restaurant. From Fall 1965 - April 1966, Alice operated her first restaurant, called the Back
			Room–this is the one that the song was written about and it was located at the site of the
			current Theresa’s Stockbridge Cafe in downtown Stockbridge. Then from 1971 - ?, she ran her
			second restaurant, Take Out Alice, in a former package store which is now a private home at 43
			Glendale Rd in Housatonic. Then she bought Avaloch from Ross in September 1975 and ran it
			through the summer of 1978. I have met many lovely people who worked and partied at the hotel
			in the Alice era. By the end I think she was sick of the place and left the area forever,
			relocating to Provincetown where she died in November 2024. I was lucky enough to get to meet
			her for the first time two days before she died and saw for myself a small glimpse of the
			fiery energy that made her famous.
		</p>

		<figure>
			<img src="../history/image6.png" />
			<figcaption>Photo of Alice from the Alice’s Restaurant Cookbook, 1969</figcaption>
		</figure>

		<p>
			In 1979 the inn was purchased from the bank by Alan Carr, who ran it under the name Portofino
			for three years and I know very little about this period so far.
		</p>

		<p>
			In 1983 it was bought by a couple from New York — the Smiths. The Smiths operated the tavern
			as the McIntosh Tavern and moved the pool to its current, beautiful location. In 1995, after
			12 years of ownership, they were looking to sell so they hired a broker-friend from New York,
			Joel Catalano, whose wife, Sharon Walker, loved the property so much that she and Joel ended
			up buying it themselves.
		</p>

		<p>
			In 2021, after 25 years, Joel & Sharon sold the hotel to Max and James Khaghan, two brothers
			from New York. They reimagined the tavern as the Ostrich Room, upgraded to more modern decor,
			installed fast Wi-Fi, and — crucially — connected the buildings to the town sewer!
		</p>

		<p>
			And in April 2024, Claire bought the hotel from them and became obsessed with its history in
			the process!
		</p>

