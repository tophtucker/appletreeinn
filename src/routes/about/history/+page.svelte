		<h1>More than you ever wanted to know about the Apple Tree Inn</h1>

		<p>
			The Apple Tree Inn has a long and fun history, and uncovering it is an ongoing treasure hunt
			for me! The property is currently 21.5 acres and has 3 structures, but has been through many
			different iterations over the last 140 years:
		</p>

		<p>I don’t know anything yet about the property before 1885.</p>

		<p>I’ll start by talking about the western half of the property:</p>

		<p>
			In 1885, a woman named Cecile Bristed built the building we call the Main House. She used it
			as a vacation home and called it The Orchard. Bristed was a great-grandaughter of John Jacob
			Astor through his first daughter Magdalena and though the circumstances of Cecile’s birth are
			somewhat unclear to me (I suspect she may have been born out of wedlock), she shows up in
			family wills and in the social pages as a fancy dinner party guest here and there. The house
			was designed by the noted Pittsfield architect H. Neill Wilson, who also designed the
			neighboring estate Shadowbrook (the one-time the largest private house in America and last
			residence of Andrew Carnegie. It burned in 1956) and Cecile’s brother Charles Astor Bristed’s
			house just down the road, Lakeside (still standing). There was also a large carriage house
			located behind the house that I assume was built at the same time the house was built but I do
			not know. It is no longer standing but I think it was located where the upper parking lot is
			now.
		</p>

		<p>
			In 1892, Bristed sold the home to Clarence Andrews (whose wife eventually divorced him and
			married the son of Chester A. Arthur, 21st president). I don’t know much else about Clarence
			Andrews yet.
		</p>

		<p>
			In 1898, Andrews sold it to a member of an old New York family, Henry Pease, and his wife
			Katharine di Pollone, the daughter of Camille di Pollone, the Count of Turin (Italy). Their
			1895 wedding had been touted by the New York Times as “the most brilliant wedding that had
			taken place in Stockbridge for many years.” Pease attended Harvard and Columbia and practiced
			law for two years before “retiring” and presumably living on his family fortune. When they
			moved in, the Peases added a billiards room and a master suite for themselves (now the Ostrich
			Room and the luxurious Room 8 above it). The Peases were definitely a “couple about town,” and
			they show up in the guestbooks of many prominent families of the era including their friends
			the Vanderbilts. They did not have any children. The Peases vacationed at The Orchard for more
			than three decades. Henry died in the home in August 1937 and Katharine sold it in November
			1946.
		</p>

		<figure>
			<img src="../history/image1.png" />
			<figcaption>The Orchard photographed in 1938</figcaption>
		</figure>

		<p>Now we will switch gears and look into the eastern half of the property.</p>

		<p>
			Around 1890, a man named Henry Barclay built a vacation house on the eastern part property and
			called it Bonnie Brae (not to be confused with Bonnie Brier in Stockbridge). It is no longer
			standing–it was located where the event lawn now is–but its carriage house is still there: it
			is the hotel’s current maintenance garage and owners quarters. Bonnie Brae was an especially
			beautiful house with 20 rooms and was the site of a number of famous balls reported on in the
			New York Times, including a notorious “stable ball” thrown in the carriage house.
		</p>

		<figure>
			<img src="../history/image4.png" />
			<figcaption>Bonnie Brae photographed by Edwin Hale Lincoln</figcaption>
		</figure>

		<p>
			I don’t know when Barclay sold. At some point, there was a three-cornered transaction with
			Madison Square Mortgage Co. and David Green, and the house was sold to a man named Eliphalet
			Davis.
		</p>

		<p>
			In 1907, Henry Clews, the enormously wealthy head of the Henry Clews & Co. banking house, who
			was a personal friend of Abraham Lincoln and economic adviser to Ulysses Grant, bought Bonnie
			Brae as a gift for his daughter.
		</p>

		<p>
			In 1919, Clews put the house up for sale and it was purchased by Gaston Drake, a lumber
			magnate whose firm supplied much of the wood used in the development of the city of Miami. He
			renamed it Astalula (cited at least once in the press, I think erroneously, as Ashtula).
		</p>

		<p>
			On March 1, 1946, Drake sold it to C. Joralemon Rollins who, on July 7, 1946, opened it as the
			Rollins Theater School. He had moved the school to Lenox from its prior home in East Hampton,
			NY after ten years there. The school used the house as an administrative building and used the
			carriage house as its theater.
		</p>

		<figure>
			<img src="../history/image3.jpg" />
			<figcaption>
				The Carriage House (where Toph & I live) serving as the theater for the Rollins Theater
				School
			</figcaption>
		</figure>

		<p>
			In 1946 the properties were united for the first time: In November of that year, the theater
			school expanded by purchasing the Orchard to use as a residence for the director of the school
			and its carriage house (no longer standing) to use as additional student housing. I think it
			was at this point that the Orchard was renamed “the West House” and Bonnie Brae was renamed
			“the East House.” The union was short-lived however, as Rollins shut the theater school in
			1948 and sold off the two parcels separately:
		</p>

		<p>
			In 1948, the East House was purchased by Lorna & Kenneth Sheldon who operated it as a B&B
			called Hawthorne Hill.
		</p>

		<p>
			I don’t understand yet what happened to the West House between 1948 and 1952, but in 1952 it
			was purchased by Roy “Skip” Rappaport who was 26 years old at the time and turned the West
			House into a summer inn called Avaloch. Avaloch is a variation of Avalon, a mythical island in
			Arthurian legend that had many apple trees on it, a nod, I think, to the original name the
			Orchard. Avaloch may also mean “above the lake,” which would be fitting considering the
			hotel’s position over the Stockbridge Bowl. In 1954, Rappaport (his mother actually) bought
			the eastern parcel from the Sheldons and the two properties were united for good.
		</p>

		<p>
			The property seemed to have something of a golden era under Rappaport who ran it for seven
			summer seasons and later led a second career as a famous anthropologist and served as chair of
			the anthropology department at University of Michigan. In a bit of foreshadowing, he brought
			Margaret Mead to the hotel as part of a speakers series in 1952. Rappaport also opened the
			famous-at-the-time Five Reasons Steak & Ale House serving beer and sandwiches until late in
			the night after Tanglewood shows.
		</p>

		<figure>
			<img src="../history/image5.jpg" />
			<figcaption>
				View of the Five Reasons Steak & Ale House. Owner Roy Rappaport is visible in blue suit at
				left of photo.
			</figcaption>
		</figure>

